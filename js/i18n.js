// اكتشاف اللغة: URL هو المرجع الوحيد
// /en/... → إنجليزي | /fr/... → فرنسي | /tr/... → تركي | /ur/... → أردو | غير ذلك → عربي
const SUPPORTED_LANGS = ['ar', 'en', 'fr', 'tr', 'ur'];
const RTL_LANGS = ['ar', 'ur'];
function _detectLang() {
    const p = window.location.pathname;
    for (const l of ['en', 'fr', 'tr', 'ur']) {
        if (p === '/' + l || p.startsWith('/' + l + '/')) return l;
    }
    return 'ar';
}
let _lang = _detectLang();

function t(key, params) {
    let v = TRANSLATIONS[_lang]?.[key];
    if (v === undefined) v = TRANSLATIONS['en']?.[key];
    if (v === undefined) v = TRANSLATIONS['ar']?.[key];
    if (v === undefined) v = key;
    // دعم placeholders البسيطة بصيغة {name}
    if (params && typeof v === 'string') {
        for (const p in params) {
            v = v.split('{' + p + '}').join(params[p]);
        }
    }
    return v;
}

function getCurrentLang() { return _lang; }
function isRTL(lang) { return RTL_LANGS.includes(lang || _lang); }

function _stripLangPrefix(path) {
    for (const l of ['en', 'fr', 'tr', 'ur']) {
        if (path === '/' + l) return '/';
        if (path.startsWith('/' + l + '/')) return path.substring(l.length + 1) || '/';
    }
    return path;
}

function setLanguage(lang) {
    if (!SUPPORTED_LANGS.includes(lang)) return;
    // التنقل بين إصدارات URL عند تغيير اللغة
    const curPath = window.location.pathname;
    const basePath = _stripLangPrefix(curPath).replace(/\.html$/, '');
    if (window.location.protocol !== 'file:') {
        localStorage.setItem('app_lang', lang);
        const prefix = (lang === 'ar') ? '' : ('/' + lang);
        const newPath = prefix + (basePath === '/' ? '/' : basePath);
        if (newPath !== curPath) {
            window.location.href = newPath + window.location.search;
            return;
        }
    }
    _lang = lang;
    localStorage.setItem('app_lang', lang);

    // تحديث اتجاه الصفحة
    document.documentElement.lang = lang;
    document.documentElement.dir  = isRTL(lang) ? 'rtl' : 'ltr';

    // تطبيق النصوص الثابتة
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const val = t(key);
        if (val && val !== key) el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const val = t(el.dataset.i18nPlaceholder);
        if (val) el.placeholder = val;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const val = t(el.dataset.i18nTitle);
        if (val) el.title = val;
    });

    // تحديث زر اللغة (الإصدار القديم — للتوافق الخلفي)
    const LANG_BTN_LABELS = { ar: 'عر', en: 'EN', fr: 'FR', tr: 'TR', ur: 'اردو' };
    document.querySelectorAll('.lang-toggle-btn').forEach(btn => {
        const order = ['ar','en','fr','tr','ur'];
        const next  = order[(order.indexOf(lang) + 1) % order.length];
        btn.textContent = LANG_BTN_LABELS[next] || 'EN';
        btn.setAttribute('data-next-lang', next);
    });
    // تحديث القائمة المنسدلة للغة إن وُجدت
    document.querySelectorAll('.lang-select').forEach(sel => {
        if (sel.value !== lang) sel.value = lang;
    });
    // تحديث مُبدّل اللغة الجديد (الأعلام + القائمة)
    _renderLangSwitcher();

    // تحديث أشهر الهجري والميلادي وأيام الأسبوع في HijriDate
    if (typeof HijriDate !== 'undefined') {
        const hm = Array.from({length:12}, (_,i) => t('hmonth.'+(i+1)));
        const gm = Array.from({length:12}, (_,i) => t('gmonth.'+(i+1)));
        const wd = Array.from({length:7},  (_,i) => t('wday.'+i));
        HijriDate.hijriMonths.splice(0, 12, ...hm);
        HijriDate.gregorianMonths.splice(0, 12, ...gm);
        HijriDate.dayNames.splice(0, 7, ...wd);
    }

    // إعادة رسم المحتوى الديناميكي
    if (typeof onLanguageChange === 'function') onLanguageChange(lang);
}

function toggleLanguage() {
    // دورة: AR → EN → FR → TR → UR → AR (للتوافق الخلفي)
    const order = ['ar', 'en', 'fr', 'tr', 'ur'];
    const next  = order[(order.indexOf(_lang) + 1) % order.length];
    setLanguage(next);
}

// ===== مُبدّل اللغة الجديد: زر العلم + قائمة منسدلة =====
// iso: رمز الدولة لعرض العلم من flagcdn.com
const LANG_META = {
    ar: { iso: 'sa', code: 'AR', name: 'العربية' },
    en: { iso: 'gb', code: 'EN', name: 'English' },
    fr: { iso: 'fr', code: 'FR', name: 'Français' },
    tr: { iso: 'tr', code: 'TR', name: 'Türkçe' },
    ur: { iso: 'pk', code: 'UR', name: 'اردو' }
};
const _FLAG_BASE = 'https://flagcdn.com/w40/';

function _renderLangSwitcher() {
    document.querySelectorAll('.lang-switcher').forEach(sw => {
        const btn = sw.querySelector('.lang-switcher-btn');
        const menu = sw.querySelector('.lang-menu');
        if (!btn || !menu) return;
        const cur = LANG_META[_lang] || LANG_META.en;
        const flagEl = btn.querySelector('.lang-flag');
        const codeEl = btn.querySelector('.lang-code');
        if (flagEl) {
            // إن كان العلم <img> نُحدّث src، وإلا نُحوّله
            if (flagEl.tagName === 'IMG') {
                flagEl.src = _FLAG_BASE + cur.iso + '.png';
                flagEl.alt = cur.code;
            } else {
                const img = document.createElement('img');
                img.className = 'lang-flag';
                img.src = _FLAG_BASE + cur.iso + '.png';
                img.alt = cur.code;
                img.width = 20; img.height = 15;
                img.loading = 'eager';
                flagEl.replaceWith(img);
            }
        }
        if (codeEl) codeEl.textContent = cur.code;
        // بناء عناصر القائمة: كل اللغات عدا الحالية
        menu.innerHTML = SUPPORTED_LANGS
            .filter(l => l !== _lang)
            .map(l => {
                const m = LANG_META[l];
                return `<button class="lang-menu-item" role="menuitem" type="button" data-lang="${l}">
                            <img class="lang-flag" src="${_FLAG_BASE}${m.iso}.png" alt="${m.code}" width="22" height="16" loading="lazy">
                            <span class="lang-name">${m.name}</span>
                        </button>`;
            }).join('');
        menu.querySelectorAll('.lang-menu-item').forEach(mi => {
            mi.addEventListener('click', (e) => {
                e.stopPropagation();
                sw.classList.remove('open');
                btn.setAttribute('aria-expanded', 'false');
                setLanguage(mi.dataset.lang);
            });
        });
    });
}

function toggleLangMenu(btn) {
    const sw = btn.closest('.lang-switcher');
    if (!sw) return;
    const isOpen = sw.classList.toggle('open');
    btn.setAttribute('aria-expanded', String(isOpen));
    // إغلاق أي قوائم أخرى مفتوحة
    document.querySelectorAll('.lang-switcher.open').forEach(other => {
        if (other !== sw) {
            other.classList.remove('open');
            const b = other.querySelector('.lang-switcher-btn');
            if (b) b.setAttribute('aria-expanded', 'false');
        }
    });
}

// إغلاق القائمة عند النقر خارجها أو Escape
document.addEventListener('click', (e) => {
    document.querySelectorAll('.lang-switcher.open').forEach(sw => {
        if (!sw.contains(e.target)) {
            sw.classList.remove('open');
            const b = sw.querySelector('.lang-switcher-btn');
            if (b) b.setAttribute('aria-expanded', 'false');
        }
    });
});
document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') {
        document.querySelectorAll('.lang-switcher.open').forEach(sw => {
            sw.classList.remove('open');
            const b = sw.querySelector('.lang-switcher-btn');
            if (b) b.setAttribute('aria-expanded', 'false');
        });
    }
});

