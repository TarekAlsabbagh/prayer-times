// اكتشاف اللغة: URL هو المرجع الوحيد
// /en/... → إنجليزي | /fr/... → فرنسي | /tr/... → تركي | /ur/... → أردو | /de/... → ألماني | /id/... → إندونيسي | غير ذلك → عربي
const SUPPORTED_LANGS = ['ar', 'en', 'fr', 'tr', 'ur', 'de', 'id', 'es', 'bn', 'ms'];
const RTL_LANGS = ['ar', 'ur'];
function _detectLang() {
    const p = window.location.pathname;
    for (const l of ['en', 'fr', 'tr', 'ur', 'de', 'id', 'es', 'bn', 'ms']) {
        if (p === '/' + l || p.startsWith('/' + l + '/')) return l;
    }
    return 'ar';
}
let _lang = _detectLang();

function t(key, params) {
    let v = TRANSLATIONS[_lang]?.[key];
    if (v === undefined) v = TRANSLATIONS['en']?.[key];
    if (v === undefined) v = TRANSLATIONS['ar']?.[key];
    if (v === undefined) v = key;
    // دعم placeholders البسيطة بصيغة {name}
    if (params && typeof v === 'string') {
        for (const p in params) {
            v = v.split('{' + p + '}').join(params[p]);
        }
    }
    return v;
}

function getCurrentLang() { return _lang; }
function isRTL(lang) { return RTL_LANGS.includes(lang || _lang); }

function _stripLangPrefix(path) {
    for (const l of ['en', 'fr', 'tr', 'ur', 'de', 'id', 'es', 'bn', 'ms']) {
        if (path === '/' + l) return '/';
        if (path.startsWith('/' + l + '/')) return path.substring(l.length + 1) || '/';
    }
    return path;
}

function setLanguage(lang) {
    if (!SUPPORTED_LANGS.includes(lang)) return;
    // التنقل بين إصدارات URL عند تغيير اللغة
    const curPath = window.location.pathname;
    const basePath = _stripLangPrefix(curPath).replace(/\.html$/, '');
    if (window.location.protocol !== 'file:') {
        localStorage.setItem('app_lang', lang);
        const prefix = (lang === 'ar') ? '' : ('/' + lang);
        const newPath = prefix + (basePath === '/' ? '/' : basePath);
        if (newPath !== curPath) {
            window.location.href = newPath + window.location.search;
            return;
        }
    }
    _lang = lang;
    localStorage.setItem('app_lang', lang);

    // تحديث اتجاه الصفحة
    document.documentElement.lang = lang;
    document.documentElement.dir  = isRTL(lang) ? 'rtl' : 'ltr';

    // تطبيق النصوص الثابتة
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const val = t(key);
        if (val && val !== key) el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        const val = t(key);
        if (val && val !== key) el.placeholder = val;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.dataset.i18nTitle;
        const val = t(key);
        if (val && val !== key) el.title = val;
    });

    // تحديث زر اللغة (الإصدار القديم — للتوافق الخلفي)
    const LANG_BTN_LABELS = { ar: 'عر', en: 'EN', fr: 'FR', tr: 'TR', ur: 'اردو', de: 'DE', id: 'ID', es: 'ES', bn: 'BN', ms: 'MS' };
    document.querySelectorAll('.lang-toggle-btn').forEach(btn => {
        const order = ['ar','en','fr','tr','ur','de','id','es','bn','ms'];
        const next  = order[(order.indexOf(lang) + 1) % order.length];
        btn.textContent = LANG_BTN_LABELS[next] || 'EN';
        btn.setAttribute('data-next-lang', next);
    });
    // تحديث القائمة المنسدلة للغة إن وُجدت
    document.querySelectorAll('.lang-select').forEach(sel => {
        if (sel.value !== lang) sel.value = lang;
    });
    // تحديث مُبدّل اللغة الجديد (الأعلام + القائمة)
    _renderLangSwitcher();

    // تحديث أشهر الهجري والميلادي وأيام الأسبوع في HijriDate
    if (typeof HijriDate !== 'undefined') {
        const hm = Array.from({length:12}, (_,i) => t('hmonth.'+(i+1)));
        const gm = Array.from({length:12}, (_,i) => t('gmonth.'+(i+1)));
        const wd = Array.from({length:7},  (_,i) => t('wday.'+i));
        HijriDate.hijriMonths.splice(0, 12, ...hm);
        HijriDate.gregorianMonths.splice(0, 12, ...gm);
        HijriDate.dayNames.splice(0, 7, ...wd);
    }

    // إعادة رسم المحتوى الديناميكي
    if (typeof onLanguageChange === 'function') onLanguageChange(lang);
}

function toggleLanguage() {
    // دورة: AR → EN → FR → TR → UR → DE → ID → ES → BN → MS → AR (للتوافق الخلفي)
    const order = ['ar', 'en', 'fr', 'tr', 'ur', 'de', 'id', 'es', 'bn', 'ms'];
    const next  = order[(order.indexOf(_lang) + 1) % order.length];
    setLanguage(next);
}

